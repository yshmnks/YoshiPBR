#include "YoshiPBR/ysStructures.h"

const ysSceneId     ys_nullSceneId      = { ys_nullIndex };
const ysShapeId     ys_nullShapeId      = { ys_nullIndex };
const ysMaterialId  ys_nullMaterialId   = { ys_nullIndex };
const ysRenderId    ys_nullRenderlId    = { ys_nullIndex, ys_nullIndex };

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
bool operator==(const ysSceneId& a, const ysSceneId& b)
{
    return a.m_index == b.m_index;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
bool operator!=(const ysSceneId& a, const ysSceneId& b)
{
    return a.m_index != b.m_index;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
bool operator==(const ysShapeId& a, const ysShapeId& b)
{
    return a.m_index == b.m_index;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
bool operator!=(const ysShapeId& a, const ysShapeId& b)
{
    return a.m_index != b.m_index;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
bool operator==(const ysMaterialId& a, const ysMaterialId& b)
{
    return a.m_index == b.m_index;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
bool operator!=(const ysMaterialId& a, const ysMaterialId& b)
{
    return a.m_index != b.m_index;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
bool operator==(const ysRenderId& a, const ysRenderId& b)
{
    return(a.m_sceneIdx == b.m_sceneIdx) && (a.m_index == b.m_index);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
bool operator!=(const ysRenderId& a, const ysRenderId& b)
{
    return(a.m_sceneIdx != b.m_sceneIdx) || (a.m_index != b.m_index);
}