struct ysRadiance
{
    void SetInvalid()
    {
        m_value = -ysVec4_one;
        m_isFinite = false;
    }

    bool IsValid() const
    {
        return ysAllGE3(m_value, ysVec4_zero);
    }

    ysVec4 m_value;
    bool m_isFinite;
};

