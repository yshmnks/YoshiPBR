ysAABB ysTriangle::ComputeAABB() const
{
    ysAABB aabb;
    aabb.m_min = ysMin(ysMin(m_v[0], m_v[1]), m_v[2]);
    aabb.m_max = ysMax(ysMax(m_v[0], m_v[1]), m_v[2]);
    return aabb;
}

