#pragma once

#include "YoshiPBR/ysStructures.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
struct ysRay
{
    void Reset();

    ysVec4 m_origin;
    ysVec4 m_direction;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
struct ysRayCastInput
{
    ysRayCastInput()
    {
        m_origin = ysVec4_zero;
        m_direction = ysVec4_unitZ;
        m_maxLambda = ys_maxFloat;
    }

    ysVec4 m_origin;
    ysVec4 m_direction;
    ys_float32 m_maxLambda;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
struct ysRayCastOutput
{
    ysVec4 m_hitPoint;
    ysVec4 m_hitNormal;
    ys_float32 m_lambda; // hitpoint = origin + lambda * direction
};