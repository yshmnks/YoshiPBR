#pragma once

#include <mutex>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
struct ysLock
{
    void Acquire();
    bool TryAcquire();

    void Release();

    std::mutex m_mutex;
};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
struct ysScopedLock
{
    ysScopedLock(ysLock*);
    ~ysScopedLock();

    ysLock* m_lock;
};